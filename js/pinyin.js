/**
 * 将中文字符串转换为拼音首字母
 * @param {string} str 输入字符串
 * @returns {string} 拼音首字母拼接字符串，如"北京"→"bj"
 */
function toPinyin(str) {
  if (!str) return '';
  var result = '';
  for (var i = 0; i < str.length; i++) {
    var ch = str[i];
    var py = PINYIN_MAP[ch];
    if (py) result += py;
    // 非汉字字符（字母/数字/标点）直接保留
    else if (ch.charCodeAt(0) > 127 && !py) { /* 跳过未知汉字 */ }
    else result += ch;
  }
  return result.toLowerCase();
}

/**
 * 检查中文字符串是否匹配某个拼音前缀
 * @param {string} str 中文字符串
 * @param {string} prefix 拼音前缀（如 "bj"、"xian"）
 * @returns {boolean}
 */
function matchesPinyin(str, prefix) {
  if (!str || !prefix) return false;
  var py = toPinyin(str);
  // 支持全拼前缀匹配（如"xian"匹配"西安"）
  // 也支持首字母匹配（如"bj"匹配"北京"）
  return py.indexOf(prefix.toLowerCase()) === 0;
}

/**
 * 获取字符串的拼音首字母数组
 * @param {string} str 输入字符串
 * @returns {string[]} 每个汉字的首字母数组，如["b","j"]
 */
function getInitials(str) {
  if (!str) return [];
  var result = [];
  for (var i = 0; i < str.length; i++) {
    var py = PINYIN_MAP[str[i]];
    if (py) result.push(py);
  }
  return result;
}

/**
 * 对 attractions 数组进行客户端拼音模糊搜索
 * @param {Array} items 景点数组
 * @param {string} query 搜索词
 * @returns {Array} 排序后的结果（精确匹配 > 拼音首字母匹配 > 描述匹配）
 */
function fuzzySearchAttractions(items, query) {
  if (!query) return items.slice(0, 20);
  var q = query.toLowerCase().trim();
  if (!q) return items.slice(0, 20);

  // 精确匹配分数
  var scored = items.map(function(item) {
    var score = 0;
    var name = item.name || '';
    var desc = item.description || '';
    var addr = item.address || '';
    var city = item.city_name || '';

    // 完全相等
    if (name === q) score += 100;
    // 名称包含
    else if (name.indexOf(q) >= 0) score += 80;
    // 城市名包含
    else if (city.indexOf(q) >= 0) score += 60;
    // 地址包含
    else if (addr.indexOf(q) >= 0) score += 40;
    // 描述包含
    else if (desc.indexOf(q) >= 0) score += 20;

    // 拼音首字母匹配（精确）
    var namePy = toPinyin(name);
    if (namePy === q) score += 90;
    else if (namePy.indexOf(q) === 0) score += 70;
    else if (namePy.indexOf(q) > 0) score += 30;

    // 城市拼音匹配
    var cityPy = toPinyin(city);
    if (cityPy === q) score += 65;
    else if (cityPy.indexOf(q) === 0) score += 50;
    else if (cityPy.indexOf(q) > 0) score += 25;

    // 全拼匹配（如果有拼音字符串中有连续的完整拼音）
    // 如 "xian" 可以在 "西安" 的拼音 "xian" 中
    if (namePy.indexOf(q) >= 0 && score < 50) score += 15;
    if (cityPy.indexOf(q) >= 0 && score < 40) score += 10;

    return { item: item, score: score };
  });

  scored.sort(function(a, b) { return b.score - a.score; });
  return scored.filter(function(s) { return s.score > 0; }).map(function(s) { return s.item; });
}

